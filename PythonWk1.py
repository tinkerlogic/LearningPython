# Arithmetic Operators
a = 10
b = 3

print("Addition:", a + b)         # Addition: 13
print("Subtraction:", a - b)      # Subtraction: 7
print("Multiplication:", a * b)   # Multiplication: 30
print("Division:", a / b)         # Division: 3.333...
print("Modulus:", a % b)          # Modulus: 1
print("Exponentiation:", a ** b)  # Exponentiation: 10^3 = 1000
print("Floor Division:", a // b)  # Floor Division: 3 (quotient without the fractional part)




# Input: Getting user input
# The input() function reads a line from input, converts it to a string, and returns it.

# Example: Asking for user's name and age
name = input("Enter your name: ")  # Takes input from user and stores it in the variable 'name'
age = input("Enter your age: ")    # Takes input from user and stores it in the variable 'age'

# Output: Printing user input
# The print() function outputs data to the console.

print("Hello, " + name + "!")      # Concatenating strings with '+' operator
print("You are " + age + " years old.")
